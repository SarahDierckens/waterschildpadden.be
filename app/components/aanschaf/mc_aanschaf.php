<p><b>Het is eindelijk zo ver!</b> Je schildpad heeft bij jou een nieuwe thuis gevonden. 
Het begin van een mooi verhaal dat wel dertig jaar kan duren. 
<b>Nog één tip:</b> als je al de trotse bezitter bent van andere schildpadden, 
houd je nieuwe huisdier dan eerst een tijdje afgezonderd van de andere schildpadden. 
